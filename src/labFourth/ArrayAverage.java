package labFourth;

public class ArrayAverage {
    public static void main(String[] args) {
        // Инициализация массива целых чисел с некоторыми значениями.
        int[] arr = {1, 2, 3, 4, 5};

        // Инициализация переменной sum для хранения суммы элементов массива.
        int sum = 0;

        // Переменная для хранения среднего арифметического значения элементов массива.
        double average = 0.0;

        try {
            // Цикл для перебора всех элементов массива.
            for(int i = 0; i < arr.length; i++) {
                // Прибавляем значение текущего элемента массива к переменной sum.
                sum += arr[i];
            }

            // Проверка на нулевую длину массива, чтобы избежать деления на ноль.
            if (arr.length != 0) {
                // Вычисление среднего арифметического.
                average = (double) sum / arr.length;
            } else {
                // Если массив пуст, выводим соответствующее сообщение.
                System.out.println("Массив пуст, среднее арифметическое не может быть вычислено");
                return;
            }

            // Вывод среднего арифметического.
            System.out.println("Среднее арифметическое: " + average);

        } catch (ArrayIndexOutOfBoundsException e) {
            // Обработка исключения, если произошел выход за границы массива.
            System.out.println("Ошибка: выход за границы массива");
        } catch (Exception e) {
            // Обработка всех других исключений.
            System.out.println("Произошла неизвестная ошибка: " + e.getMessage());
        }
    }
}


